# Different types of variables
a = 10            # integer
b = 3.14          # float
c = "Hello"       # string
d = True          # boolean
e = [1, 2, 3]     # list

# Print the type of each variable
print("Type of a:", type(a))
print("Type of b:", type(b))
print("Type of c:", type(c))
print("Type of d:", type(d))
print("Type of e:", type(e))
